package com.bean;

public class Brand {

	private Integer bid;
	private String bname;

	/**
	 * @return the bid
	 */
	public Integer getBid() {
		return bid;
	}

	/**
	 * @param bid the bid to set
	 */
	public void setBid(Integer bid) {
		this.bid = bid;
	}

	/**
	 * @return the bname
	 */
	public String getBname() {
		return bname;
	}

	/**
	 * @param bname the bname to set
	 */
	public void setBname(String bname) {
		this.bname = bname;
	}

}
